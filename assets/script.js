function addition() {
    let firstNum = prompt( '1st number is ', "0");
    let secondNum = prompt( '2nd number is ', "0");
    alert( firstNum + " + " + secondNum + " = " + ( Number(firstNum) + Number(secondNum) ));
  }
  function multiplication() {
    let firstNum = prompt( '1st number is ', "0");
    let secondNum = prompt( '2nd number is ', "0");
    alert( firstNum + " * " + secondNum + " = " + ( Number(firstNum) * Number(secondNum) ));
  }
  
  function division() {
    let firstNum = prompt( '1st number is ', "0");
    let secondNum = prompt( '2nd number is ', "0");
    alert( firstNum + " / " + secondNum + " = " + ( Number(firstNum) / Number(secondNum) ));
  }
  
  function subtraction() {
    let firstNum = prompt( '1st number is ', "0");
    let secondNum = prompt( '2nd number is ', "0");
    alert( firstNum + " - " + secondNum + " = " + ( Number(firstNum) - Number(secondNum) ));
  }
  